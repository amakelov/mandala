def chunk_list(lst:TList[TAny], num_chunks:int=None, chunk_size:int=None) -> TList[list]:
    """
    Chunks a list of objects into either approximately-equal chunks of a given
    number, or chunks of an exact given size (with the last one possibly
    smaller).
    """
    n_elts = len(lst)
    assert (num_chunks is None) != (chunk_size is None)
    if num_chunks is not None:
        parts = np.array_split(ary=np.arange(n_elts), indices_or_sections=num_chunks)
        return [[lst[int(i)] for i in part] for part in parts]
    else:
        assert chunk_size is not None
        indices = [i*chunk_size for i in range(1, n_elts // chunk_size + 1)]
        parts = np.array_split(ary=np.arange(n_elts), indices_or_sections=indices)
        return [[lst[int(i)] for i in part] for part in parts if len(part) > 0]

