PatternVar = typing.TypeVar(name='PatternVar')
EltVar = typing.TypeVar(name='EltVar')

def group_like(objs:TList[EltVar], labels:TList[PatternVar]) -> TDict[PatternVar, TList[EltVar]]:
    """
    Group the given objects according to the given pattern values
    """
    assert len(labels) == len(objs)
    res = defaultdict(list)
    for label, obj in zip(labels, objs):
        res[label].append(obj)
    return res

def ungroup_like(groups:TDict[PatternVar, TList[EltVar]], labels:TList[PatternVar]) -> TList[EltVar]:
    """
    Turn groups back into a flat ordering of all elements according to given labels
    """
    assert len(labels) == sum(len(x) for x in groups.values())
    # {label: [indices belonging to this label]}
    index_groups = group_like(objs=list(range(len(labels))), labels=labels)
    res_dict = {index_groups[label][j]: obj
                for label in groups.keys()
                for j, obj in enumerate(groups[label])}
    res = [res_dict[i] for i in range(len(labels))]
    return res

def transpose_lists(lists:TList[TList[TAny]]) -> TList[TList[TAny]]:
    if not lists:
        return []
    assert all(len(lists[0]) == len(lists[i]) for i in range(len(lists)))
    common_len = len(lists[0])
    return [[lists[i][j] for i in range(len(lists))] for j in range(common_len)]

def transpose_returns(returns_cols:TUnion[TList[TAny], TTuple[TList[TAny],...]]) -> TUnion[TList[TTuple[TAny,...]], TList[TAny]]:
    if not isinstance(returns_cols, tuple):
        assert isinstance(returns_cols, list)
        return returns_cols
    else:
        if not returns_cols:
            return []
        assert all(len(returns_cols[0]) == len(returns_cols[i]) for i in range(len(returns_cols)))
        common_len = len(returns_cols[0])
        return [tuple([returns_cols[i][j] for i in range(len(returns_cols))]) for j in range(common_len)]

def transpose_listdict(dictlist:TDict[str, TList[TAny]]) -> TList[TDict[str, TAny]]:
    if not dictlist:
        return []
    k = list(dictlist.keys())[0]
    assert all(len(dictlist[k]) == len(dictlist[other_k]) for other_k in dictlist.keys())
    common_len = len(dictlist[k])
    return [{k: dictlist[k][i] for k in dictlist.keys()} for i in range(common_len) ]

def extract_uniques(objs:TList[TAny], keys:TList[TAny]) -> TTuple[TList[TAny], TList[int], TDict[int, int]]:
    """
    Given lists of objects and matching keys, return:
        - a list of representative objects (one per key)
        - list of indices of these objects in `objs`
        - projection from range(objs) -> range(representatives)
        mapping each object/key to its representative
    """
    assert len(objs) == len(keys)
    (
        unique_keys, # list of unique keys (no guarantee on ordering)
        index, # indices of the unique keys in `keys`
        inverse, # such that unique_keys[inverse] = keys
    ) = np.unique(keys, return_index=True, return_inverse=True)
    representatives = [objs[i] for i in index]
    projection = {i: inverse[i] for i in range(len(objs))}
    return representatives, index.tolist(), projection

