def is_admissible_atom(obj:TAny, forbidden_types:TList[type]=None) -> bool:
    """
    Recursively check if a value is constituted *only* of certain "simple" types
    """
    forbidden_types = [] if forbidden_types is None else forbidden_types
    tp = type(obj)
    if tp in forbidden_types:
        return False
    if tp in (int, float, str, bool, type(None)):
        return True
    if tp in (list, tuple, set):
        return all([is_admissible_atom(elt, forbidden_types=forbidden_types) for elt in obj])
    if tp is dict:
        return (all([is_admissible_atom(k, forbidden_types=forbidden_types) for k in obj.keys()]) 
                and all([is_admissible_atom(k, forbidden_types=forbidden_types) for k in obj.values()]))
    if tp in (np.ndarray, pd.DataFrame, pd.Series):
        return True
    return False

