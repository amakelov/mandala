"""
A `Collection` represents a recursive nesting of python lists, dictionaries, and
other values that are of neither of these types.
"""
# TAtom = typing.TypeVar('TAtom') # unclear how to make this work
Collection = TUnion[TAny, TList['Collection'], TDict[str, 'Collection']]


def get_collection_atoms(collection:Collection) -> TList[TAny]:
    """
    Return a list (no guarantees on order) with the atoms in this collection. An
    atom is any element of a list or value of a dict in this collection that is
    itself not a dict or list. 
    """
    if isinstance(collection, list):
        return concat_lists([get_collection_atoms(elt) for elt in collection])
    elif isinstance(collection, dict):
        return concat_lists([get_collection_atoms(v) for v in collection.values()])
    else:
        return [collection]

def collec_same_shape(collec_1:Collection, collec_2:Collection) -> bool:
    """
    Check if two collection objects have the same shape, meaning that all
    matching lists have the same length and all matching dicts have the same
    keys. This takes into account empty internal collections as well.
    """
    if isinstance(collec_1, list):
        if not isinstance(collec_2, list):
            return False
        if not len(collec_1) == len(collec_2):
            return False
        return all(collec_same_shape(elt_1, elt_2) for elt_1, elt_2 in zip(collec_1, collec_2))
    elif isinstance(collec_1, dict):
        if not isinstance(collec_2, dict):
            return False
        if not sorted(collec_1.keys()) == sorted(collec_2.keys()):
            return False
        return all(collec_same_shape(collec_1[k], collec_2[k]) for k in collec_1.keys())
    else:
        if isinstance(collec_2, (list, dict)):
            return False
        return True

def flatten_collection_atoms(collection:Collection, start_idx:int) -> TTuple[TList[TAny], Collection]:
    """
    Given a recursive collection, return a deterministic ordering of its atoms,
    together with a same-shape collection whose atoms are the indices of the
    corresponding atoms in the list.
    """
    if isinstance(collection, list):
        res_flat, res_collec = [], []
        cur_start = start_idx
        for elt in collection:
            elt_flat, elt_collec = flatten_collection_atoms(collection=elt, start_idx=cur_start)
            cur_start += len(elt_flat)
            res_flat += elt_flat
            res_collec.append(elt_collec)
    elif isinstance(collection, dict):
        res_flat, res_collec = [], {}
        ord_keys = sorted(collection.keys())
        cur_start = start_idx
        for k in ord_keys:
            v = collection[k]
            val_flat, val_collec = flatten_collection_atoms(collection=v, start_idx=cur_start)
            cur_start += len(val_flat)
            res_flat += val_flat
            res_collec[k] = val_collec
    else:
        res_flat, res_collec = [collection], start_idx
    return res_flat, res_collec

def unflatten_atoms_like(atoms:TList[TAny], index_reference:Collection) -> Collection:
    """
    Given a list of atoms (non-list and non-dict), and the index of a matching
    collection, unflatten these atoms into a same-shaped collection.
    """
    #! this is too slow to check recursively
    # assert all(not isinstance(atom, (list, dict)) for atom in atoms)
    if isinstance(index_reference, list):
        return [unflatten_atoms_like(atoms=atoms, index_reference=elt) 
                for elt in index_reference]
    elif isinstance(index_reference, dict):
        return {k: unflatten_atoms_like(atoms=atoms, index_reference=v)
                for k, v in index_reference.items()}
    else:
        assert isinstance(index_reference, int)
        return atoms[index_reference]

def apply_to_collection(collection:Collection, func:TCallable[[TAny], TAny]) -> Collection:
    """
    Recursively apply a function to the atoms in a collection, returning a new
    collection of the same shape
    """
    if isinstance(collection, list):
        return [apply_to_collection(collection=elt, func=func) for elt in collection]
    elif isinstance(collection, dict):
        return {k: apply_to_collection(collection=v, func=func) for k, v in collection.items()}
    else:
        return func(collection)
    
