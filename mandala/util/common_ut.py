def get_uid() -> str:
    """
    Get a random identifier which is unique with high probability from the
    operating system's randomness
    """
    return '{}'.format(binascii.hexlify(os.urandom(16)).decode('utf-8'))

def eq_objs(x:TAny, y:TAny, rtol:float=1e-5, atol:float=1e-8) -> bool:
    """
    A helper function to check when two values are equal. The rules for how
    values can be built are generated by the following:
        - lists, tuples, str-key dicts
        - scalars
        - things like arrays, frames
    """
    numpy_checker = lambda a, b: bool(np.all(np.isclose(a, b, rtol=rtol, atol=atol)))
    tpx, tpy = type(x), type(y)
    if tpx != tpy:
        failure = (f'Different types', x, y)
        return False
    if tpx is np.ndarray:
        result = numpy_checker(x, y)
        return result
    elif tpx is pd.DataFrame:
        return numpy_checker(x, y)
    elif tpx is pd.Series:
        return numpy_checker(x, y)
    elif tpx in (list, tuple):
        if len(x) != len(y):
            return False
        return all(eq_objs(xelt, yelt, rtol=rtol, atol=atol) for xelt, yelt in zip(x, y))
    elif tpx is dict:
        if len(x) != len(y):
            return False
        if x.keys() != y.keys():
            return False
        return all(eq_objs(x=x[k], y=y[k], rtol=rtol, atol=atol) for k in x.keys())
    else:
        return x == y

def all_distinct(elts:TIter[THashable]) -> bool:
    return len(list(elts)) == len(set(elts))

