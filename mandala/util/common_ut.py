def rename_dict_keys(dct:dict, mapping:dict) -> dict:
    return {mapping[k]: v for k, v in dct.items()}

def invert_dict(dct:dict) -> dict:
    return {v: k for k, v in dct.items()}

def compose_dicts(first:dict, second:dict) -> dict:
    return {k: second[v] for k, v in first.items()}


