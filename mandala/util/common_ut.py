class CausalHashing(object):
    
    @staticmethod
    def update_with_dict(m, d:TDict[str, str]):
        """
        deterministically update a `hashlib.md5()` object with the content of a dictionary.
        """
        for k in sorted(d):
            m.update(k.encode())
            m.update(d[k].encode())

    @staticmethod
    def hash_computation(input_hashes:TDict[str, str], metadata:TDict[str, str]) -> str:
        m = hashlib.md5()
        CausalHashing.update_with_dict(m=m, d=input_hashes)
        CausalHashing.update_with_dict(m=m, d=metadata)
        return m.hexdigest()
    
    @staticmethod
    def hash_outputs(computation_hash:str, output_names:TList[str]) -> str:
        m = hashlib.md5(computation_hash.encode())
        for name in output_names:
            m.update(name.encode())
        return m.hexdigest()

    @staticmethod
    def hashforward_list(cur_hash:str, idx:int) -> str:
        m = hashlib.md5(cur_hash.encode())
        m.update(str(idx).encode())
        return m.hexdigest()

    @staticmethod
    def hashforward_dict(cur_hash:str, key:str) -> str:
        m = hashlib.md5(cur_hash.encode())
        m.update(key.encode())
        return m.hexdigest()

    @staticmethod
    def hashforward_tuple(cur_hash:str, idx:int) -> str:
        m = hashlib.md5(cur_hash.encode())
        m.update(str(idx).encode())
        return m.hexdigest()

    @staticmethod
    def combine_list(elt_hashes:TList[str]) -> str:
        m = hashlib.md5('list'.encode())
        s = str(elt_hashes)
        m.update(s.encode())
        return m.hexdigest()
        
    @staticmethod
    def combine_dict(hashes_dict:TDict[str, str]) -> str:
        m = hashlib.md5('dict'.encode())
        key_order = sorted(hashes_dict)
        s = str([(k, hashes_dict[k]) for k in key_order])
        m.update(s.encode())
        return m.hexdigest()

    @staticmethod
    def combine_tuple(elt_hashes:TTuple[str,...]) -> str:
        m = hashlib.md5('tuple'.encode())
        s = str(elt_hashes)
        m.update(s.encode())
        return m.hexdigest()
